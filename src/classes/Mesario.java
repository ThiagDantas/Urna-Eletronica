public class Mesario{
    
    String idMesario;
    String nomeMesario;
    String secaoMesario;
    String papel;
    String loginMesario;
    String SenhaMesario;

    public String getNomeMesario() {
        return nomeMesario;
    }

    public void setNomeMesario(String nomeMesario) {
        this.nomeMesario = nomeMesario;
    }

    public String getIdMesario() {
        return idMesario;
    }

    public void setIdMesario(String idMesario) {
        this.idMesario = idMesario;
    }

    public String getSecaoMesario() {
        return secaoMesario;
    }

    public void setSecaoMesario(String secaoMesario) {
        this.secaoMesario = secaoMesario;
    }

    public String getPapel() {
        return papel;
    }

    public void setPapel(String papel) {
        this.papel = papel;
    }

    public String getLoginMesario() {
        return loginMesario;
    }

    public void setLoginMesario(String loginMesario) {
        this.loginMesario = loginMesario;
    }

    public String getSenhaMesario() {
        return SenhaMesario;
    }

    public void setSenhaMesario(String SenhaMesario) {
        this.SenhaMesario = SenhaMesario;
    }
    
}
